package sukkiri.practice.practice_print05.practice02;

public class Main {
	/*
	 *メソッド名:printMs
	 *引数:String message
	 *戻り値:なし
	 *処理:引数で渡された文字列を出力する
	 */
	public static void printMs(String message) {
		
		System.out.println(message);
	}
	
	public static void main(String[] args) {
		
		printMs("こんにちは");
	}

}
